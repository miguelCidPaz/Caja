    private void BtnAñadirClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAñadirClienteActionPerformed
        // TODO add your handling code here:
        ClienteHabitual cliente = new ClienteHabitual();
        ClientesHabitualesService ch = new ClientesHabitualesService();
        cliente.setNombre(nombreTXT.getText()); cliente.setDNI(DNITXT.getText());
        cliente.setEmail(emailTXT.getText()); cliente.setTelefono(telefonoTXT.getText());
        try {
            ch.añadircliente(cliente);
        } catch (SQLException ex) {
            System.out.println("Error en boton añadir cliente :"+ex);
        }
    }//GEN-LAST:event_BtnAñadirClienteActionPerformed

    private void BtnModificarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnModificarClienteActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tm = (DefaultTableModel)tablaClientes.getModel();
        String dato = String.valueOf(tm.getValueAt(tablaClientes.getSelectedRow(), 0));
        int id = Integer.parseInt(dato);
        
        ClienteHabitual cliente = new ClienteHabitual();
        ClientesHabitualesService ch = new ClientesHabitualesService();
        cliente.setIdCliente(id); cliente.setEmail(emailTXT.getText()); cliente.setTelefono(telefonoTXT.getText());
        
        try {
            ch.modificarcliente(cliente);
        } catch (SQLException ex) {
            System.out.println("Error en modificacion cliente"+ex);
        }
    }//GEN-LAST:event_BtnModificarClienteActionPerformed

