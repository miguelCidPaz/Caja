    private void BtnEliminarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEliminarClienteActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tm = (DefaultTableModel)tablaClientes.getModel();
        String dato = String.valueOf(tm.getValueAt(tablaClientes.getSelectedRow(), 0));
        int id = Integer.parseInt(dato);
        
        ClienteHabitual cliente = new ClienteHabitual();
        ClientesHabitualesService ch = new ClientesHabitualesService();
        cliente.setIdCliente(id);
        
        try {
            ch.borrarcliente(cliente);
        } catch (SQLException ex) {
            System.out.println("Error en borrar cliente"+ex);
        }
    }//GEN-LAST:event_BtnEliminarClienteActionPerformed

