    private void BtnAñadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAñadirActionPerformed

        Empleado empleado = new Empleado();
        EmpleadoService es = new EmpleadoService();
        empleado.setNombre(nombreTXT.getText()); empleado.setDNI(DNITXT.getText());
        empleado.setEmail(emailTXT.getText()); empleado.setTelefono(telefonoTXT.getText());
        try{
            es.añadirEmpleado(empleado);
        }catch(SQLException e){
            System.out.println("Error al añadir empleado "+e);
        }
        try {
            es.tablaempleados(tablaEmpleados);
        } catch (SQLException ex) {
            System.out.println("Error en actualizacion automatica");
        }
    }//GEN-LAST:event_BtnAñadirActionPerformed

    private void BtnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnModificarActionPerformed

        int id = devolverid();
        
        Empleado empleado = new Empleado();
        EmpleadoService es = new EmpleadoService();
        empleado.setIdempleado(id); empleado.setEmail(emailTXT.getText()); empleado.setTelefono(telefonoTXT.getText());
        
        try{
            es.modificarempleado(empleado);
        }catch(SQLException e){
            System.out.println("Error en modificacion de empleado "+e);
        }
        try {
            es.tablaempleados(tablaEmpleados);
        } catch (SQLException ex) {
            System.out.println("Error en actualizacion automatica");
        }
    }//GEN-LAST:event_BtnModificarActionPerformed

