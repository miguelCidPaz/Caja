    private void BtnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnBorrarActionPerformed
        
        int id = devolverid();
        Empleado empleado = new Empleado();
        EmpleadoService es = new EmpleadoService();
        empleado.setIdempleado(id);
        try{
            es.borrarempleado(empleado);
        }catch(SQLException e){
            System.out.println("Error al borrar empleado");
        }
        try {
            es.tablaempleados(tablaEmpleados);
        } catch (SQLException ex) {
            System.out.println("Error en actualizacion automatica");
        }
            
    }//GEN-LAST:event_BtnBorrarActionPerformed

    private int devolverid(){
        DefaultTableModel tm = (DefaultTableModel)tablaEmpleados.getModel();
        String dato = String.valueOf(tm.getValueAt(tablaEmpleados.getSelectedRow(), 0));
        int id = Integer.parseInt(dato);
        
        return id;
        
    }
